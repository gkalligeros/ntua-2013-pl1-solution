public class node {

	private int x;
	private int y;
	private int dst;

	public node(int x, int y, int dst) {
		super();
		this.x = x;
		this.y = y;
		this.dst = dst;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getDst() {
		return dst;
	}

	public void setDst(int dst) {
		this.dst = dst;
	}

}
